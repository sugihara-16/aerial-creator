"""One-generation/one-update production PPO runner for Order 9.

Collection is intentionally a separate real-Isaac operation.  This runner
accepts one immutable stochastic rollout manifest generated by the immediate
parent checkpoint, performs exactly one PPO update, and emits a new strict
checkpoint.  Reusing any rollout generation in the checkpoint lineage fails
closed.
"""

